/* given a character, return a pointer to
the bitmap for that character */
/* chars have a value of 0x30 to 0x3a */
/* bitmap spaceing is 12 bytes per character */

int index;
char *getbitmap(char c)
{
    index = (c - 0x30) * 12;
    return &bitmap[index];
}
